# if __name__ == '__main__': ブロックは、Pythonスクリプトが直接実行された場合にのみ特定のコードを実行するために使用されます。
# これは通常、スクリプトをモジュールとして他のスクリプトからインポートする場合に、特定のコードが実行されないようにするために役立ちます。以下に具体的な例を示します。

# このスクリプトが直接実行された場合にのみ実行されるコードを定義
def main():
    print("このスクリプトが直接実行されました。")

# このブロックは、このスクリプトが直接実行された場合にのみ実行される
if __name__ == '__main__':
    main()

# 上記を実行したら "このスクリプトが直接実行されました。" と出力される。
# この例では、main() 関数が定義されており、if __name__ == '__main__': ブロック内で main() 関数が呼び出されています。したがって、このスクリプトを直接実行した場合にのみ、"このスクリプトが直接実行されました。" というメッセージが表示されます。
# しかし、このスクリプトが他のスクリプトからインポートされた場合には、main() 関数は実行されず、その他の関数や変数はインポート可能です。このようにして、スクリプトがモジュールとして再利用できるようになります。
